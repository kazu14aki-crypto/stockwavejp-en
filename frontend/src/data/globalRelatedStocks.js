export function getGlobalStocksForThemes(themes = []) {
  const seen = new Set()
  const rows = []
  for (const theme of themes) {
    for (const row of GLOBAL_THEME_STOCKS[theme] || []) {
      if (!seen.has(row[0])) {
        seen.add(row[0])
        rows.push({ ticker:row[0], name:row[1], strength:row[2], risk:row[3], theme })
      }
    }
  }
  return rows
}
