def translate_theme(name_ja: str) -> str:
    """テーマ名を英語に変換"""
    return THEME_NAME_EN.get(name_ja, name_ja)


def translate_stock(name_ja: str) -> str:
    """銘柄名を英語に変換"""
    return STOCK_NAME_EN.get(name_ja, name_ja)


def translate_themes_dict(themes: dict) -> dict:
    """DEFAULT_THEMESを英語キーに変換して返す"""
    result = {}
    for theme_ja, stocks in themes.items():
        theme_en = translate_theme(theme_ja)
        stocks_en = {translate_stock(k): v for k, v in stocks.items()}
        result[theme_en] = stocks_en
    return result


